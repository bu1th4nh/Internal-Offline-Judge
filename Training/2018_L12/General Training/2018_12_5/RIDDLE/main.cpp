/*
           ---------Problem briefing---------
    Given:  * k goods w/ price from 1 to k
            * n bills: a1..an

    Find m such that with m bills from <a1...am>,
    you can buy any goods from 1 to k w/o charges.
    If there is multiple solution, print the smallest one.
           ----------------------------------

           -------------Solution-------------

    Assume that we can buy anything which costs from 1 to T with
    first i-1-th bills. If we add one more bill w/ value a[i],
    there will be 2 cases:

        * a[i] <= T + 1: We can buy anything which costs
        from 1 to T + a[i].
        * Otherwise, we still only buy anything which costs
        from 1 to T.

    So, we'll come up with the solution for the problem:
        * Initialize T = 0
        * Iterate from 1 to n, push a[i] into min heap. If
        top_heap <= T + 1, we increase T <---- T + top_heap
        and pop top_heap from heap. Then we check if T >= k,
        return. Otherwise we continues until n or T >= k.

           ----------------------------------

*/
