//Check
bool check()
{
    if(st.size() > 100) return 0;
    int last = 0, i = -1;

    for(auto x: st)
    {
        ++i;
        if(i == 0) last = x;
        else
        {
            if(x != last + 1) return 0;
            last = x;
        }
    }
    return 1;
}
ll BFS()
{
    int u;
    queue<int> que;
    ll ret = 1LL;

    que.push(1);
    st.insert(a[1]);

    while(!que.empty())
    {
        u = que.front();
        que.pop();

        for(int v: adj[u])
        {
            que.push(v);
            st.insert(a[v]);

            ret += (check() && valid[v]);
        }

    }
    return ret;
}
void DFS(int u)
{
    st.insert(a[u]);

    valid[u] = check();
    for(int v: adj[u]) DFS(v);
    st.erase(a[u]);
}

//Output
void is_that_true()
{
    res = 0LL;
    DFS(1);

    st.clear();
    res = BFS();
    cout << res << el;
}

