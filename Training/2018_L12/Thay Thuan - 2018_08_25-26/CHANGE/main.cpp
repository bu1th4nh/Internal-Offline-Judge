//Process
int TopDown(int i, int x)
{
    if(x < 0) return inf;
    if(x == 0) return 0;
    if(i == -1) return inf;

    if(dp[i][x] != -1) return dp[i][x];

    int res = inf;
    res = min(res, TopDown(i, x-a[i]) + 1);
    res = min(res, TopDown(i-1, x));

    dp[i][x] = res;
    return res;
}

