ll power(ll a, ll b)
{
    if(b == 0) return 1LL;
    if(b == 1) return 1LL * (a % p);

    ll m416 = power(a, b >> 1);

    return (b & 1) ? (((1LL * a) % p) * ((m416 * m416) % p)) % p : ((m416 * m416) % p);
}   //Don't f**king fix this code. It is f**king correct :)

